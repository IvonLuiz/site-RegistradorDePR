                <h1 class="text-center text-md-start mt-5">
                    Levantamento Terra
                </h1>
                
                <p class="lead text-center text-md-start">
                    O levantamento terra, também conhecido como deadlift, é um dos exercícios mais efetivos para desenvolver a força geral do corpo, especialmente os músculos das costas, glúteos, isquiotibiais, quadríceps e core. Aqui estão algumas dicas para executar um bom levantamento terra:
                </p>
                
                <ul class="lead text-center text-md-start">
                    
                    <li>Posição inicial: Coloque a barra no chão com os pés afastados na largura dos ombros. Posicione os pés de forma que fiquem alinhados com a barra, mantendo os dedos dos pés apontados ligeiramente para fora.</li>

                    <li>Agarre: Agache-se e segure a barra com as mãos, de preferência em uma empunhadura mista (uma palma para cima e outra para baixo), para aumentar a aderência. As mãos devem ficar ligeiramente mais largas do que a largura dos ombros.</li>

                    <li>Postura: Mantenha as costas retas, o peito para cima e os ombros para trás. Evite curvar a coluna durante o movimento, pois isso pode levar a lesões nas costas.</li>

                    <li>Puxe com as pernas: Ao levantar a barra, concentre-se em empurrar o chão com os pés enquanto levanta o corpo. Isso ajudará a manter uma postura adequada e envolverá os músculos das pernas no movimento.</li>

                    <li>Levante os quadris: À medida que a barra passa pelos joelhos, levante os quadris para completar o levantamento. Mantenha a barra próxima ao corpo durante todo o movimento.</li>

                    <li>Não hiperestenda a coluna: Ao atingir a posição vertical, não hiperestenda a coluna (não jogue o quadril para frente). Mantenha o corpo alinhado e estabilizado.</li>

                    <li>Descida controlada: Ao abaixar a barra de volta ao chão, faça isso de forma controlada e mantendo a postura correta. Evite soltar a barra de maneira brusca.</li>

                    <li>Respiração: Assim como nos outros levantamentos, antes de iniciar o movimento, encha os pulmões de ar e mantenha a respiração durante o levantamento. Expire ao final do movimento.</li>

                </ul>

                <p class="lead text-center text-md-start">
                    É essencial praticar o levantamento terra com cautela e começar com cargas mais leves, especialmente se você é iniciante. A forma adequada é crucial para evitar lesões, pois o levantamento terra coloca uma quantidade significativa de pressão na coluna vertebral.
                </p>

                <p class="lead text-center text-md-start">
                    No powerlifting, os recordes no levantamento terra variam de acordo com a categoria de peso e a federação em que os atletas competem. Em categorias de peso mais elevadas, os recordes podem superar 400 kg ou mais. No entanto, assim como em qualquer levantamento de peso, a segurança e a técnica adequada devem ser priorizadas em relação ao levantamento de cargas extremamente pesadas. Sempre trabalhe em conjunto com um treinador experiente para garantir que você esteja progredindo de forma segura e eficiente.
                </p>

