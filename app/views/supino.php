                    Supino
                </h1>
                
                <p class="lead text-center text-md-start">
                    O supino é outro exercício fundamental no treinamento de força, especialmente para o desenvolvimento dos músculos peitorais, ombros e tríceps. Aqui estão algumas dicas para executar um bom supino:
                </p>
                
                <ul class="lead text-center text-md-start">
                    
                    <li>Posição no banco: Deite-se em um banco plano ou inclinado, garantindo que suas costas, cabeça e glúteos estejam apoiados no banco. Mantenha os pés firmemente apoiados no chão para obter estabilidade.</li>

                    <li>Empunhadura: A largura das mãos na barra pode variar de pessoa para pessoa, mas uma boa regra é posicionar as mãos de forma que os antebraços fiquem perpendiculares ao chão quando a barra estiver na posição de toque no peito. A empunhadura pode ser pronada (palmas viradas para os pés) ou supinada (palmas viradas para a cabeça), dependendo da preferência pessoal.</li>

                    <li>Descida controlada: Ao abaixar a barra em direção ao peito, faça isso de forma controlada. Evite quedas bruscas ou desequilíbrios que possam causar lesões. Mantenha os cotovelos ligeiramente abaixo dos ombros durante a descida.</li>

                    <li>Toque no peito: Ao atingir o ponto mais baixo do movimento, deixe a barra tocar suavemente o peito, controlando o movimento para evitar impactos muito fortes.</li>

                    <li>Empurrar com força: Ao empurrar a barra para cima, concentre-se em usar a força dos músculos do peito, ombros e tríceps. Mantenha os cotovelos alinhados com o corpo durante a fase de subida.</li>

                    <li>Respiração: Antes de começar o movimento, encha os pulmões de ar e mantenha a respiração durante a fase de descida e empurre com força ao expirar. Isso ajudará a estabilizar o core e a fornecer mais força durante o movimento.</li>

                    <li>Não bloqueie os cotovelos: Ao estender os braços completamente, não bloqueie os cotovelos. Mantenha-os levemente flexionados para reduzir a tensão nas articulações.</li>

                    <li>Progressão: Assim como no agachamento, comece com cargas mais leves e vá aumentando gradualmente à medida que você se sentir mais confortável com o movimento e a técnica.</li>

                </ul>

                <p class="lead text-center text-md-start">
                    Recordes no supino variam de acordo com a categoria de peso e a federação de powerlifting em que os levantadores competem. No nível profissional e de elite, os recordes podem ultrapassar 300 kg em categorias de peso mais pesadas. No entanto, é importante lembrar que, independentemente dos números impressionantes, a execução adequada do exercício é fundamental para evitar lesões e alcançar um progresso seguro e sustentável no treinamento de força.
