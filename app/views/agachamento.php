                <h1 class="text-center text-md-start mt-5">
                    Agachamento
                </h1>
                
                <p class="lead text-center text-md-start">
                    Executar um bom agachamento é essencial para qualquer pessoa interessada em treinamento de força e, em particular, para aqueles envolvidos em powerlifting. O agachamento é um exercício composto que trabalha os músculos das pernas, glúteos e o core, além de desenvolver a estabilidade e a força global do corpo. Aqui estão algumas dicas para executar um agachamento de forma adequada:
                </p>
                
                <ul class="lead text-center text-md-start">
                    
                    <li>Postura correta: Posicione os pés na largura dos ombros ou um pouco mais afastados. Mantenha os pés apontando levemente para fora. Mantenha a coluna reta, o peito para cima e os ombros para trás.</li>

                    <li>Profundidade: Ao agachar, busque uma boa amplitude de movimento. O ideal é que a coxa fique, no mínimo, paralela ao chão ou abaixo disso. Isso ajuda a trabalhar todos os músculos envolvidos e evita tensões desnecessárias nas articulações.</li>

                    <li>Mantenha os joelhos alinhados: Ao descer, certifique-se de que seus joelhos sigam a mesma direção dos pés e não desloquem para dentro ou para fora. Isso ajuda a prevenir lesões nos joelhos.</li>

                    <li>Controle do movimento: Evite rebotes ou descidas rápidas demais. Procure manter o controle em todas as fases do exercício para evitar lesões e garantir que os músculos sejam realmente ativados.</li>

                    <li>Respiração adequada: Antes de começar o movimento, encha os pulmões de ar e mantenha a respiração durante o agachamento. Isso ajuda a estabilizar o core e proporcionar mais força durante o movimento.</li>

                    <li>Progressão gradual: Se você é iniciante ou está retornando após um tempo de inatividade, comece com cargas mais leves e vá aumentando gradualmente. Dominar a técnica é mais importante do que levantar grandes pesos.</li>

                </ul>

                <p class="lead text-center text-md-start">
                    Em relação ao powerlifting, é um esporte de levantamento de peso que consiste em três movimentos principais: o agachamento, o supino e o levantamento terra (deadlift). O objetivo é levantar o maior peso possível em cada um desses movimentos. Cada competidor tem três tentativas em cada movimento, e a melhor tentativa de cada um é somada para determinar o total levantado, que é o recorde pessoal do atleta naquele evento.
                </p>

                <p class="lead text-center text-md-start">
                    Existem também recordes mundiais no powerlifting, que são as maiores marcas já alcançadas por atletas em competições sancionadas. Esses recordes são geralmente estabelecidos por atletas de elite, que possuem uma combinação excepcional de força, técnica e dedicação ao treinamento.
                </p>

                <p class="lead text-center text-md-start">
                    Os recordes mundiais no powerlifting variam dependendo da categoria de peso e da federação de powerlifting em questão. Além disso, é importante mencionar que o esporte continua evoluindo, e novos recordes são estabelecidos periodicamente.
                </p>

                <p class="lead text-center text-md-start">
                    Para se ter uma ideia dos números impressionantes alcançados no powerlifting, os recordes mundiais podem superar, em alguns casos, 400 kg no agachamento, 300 kg no supino e 450 kg no levantamento terra para atletas de peso mais elevado. É uma demonstração impressionante de força e habilidade humana, mas é importante lembrar que o powerlifting é um esporte que exige treinamento sério e acompanhamento adequado para evitar lesões e garantir a progressão segura.
                </p>
